/**
 * Icon component - migrated from theme/snippets/icon.liquid
 *
 * A centralized SVG icon system with consistent styling.
 *
 * @example
 * <Icon name="cart" />
 *
 * @example
 * <Icon name="heart" size={20} className="text-red-500" />
 *
 * @example
 * <Icon name="search" ariaLabel="Search" />
 */
export function Icon({
  name,
  size = 24,
  strokeWidth = 2,
  ariaLabel,
  className,
  ...rest
}: IconProps) {
  const isDecorative = !ariaLabel;

  return (
    <svg
      className={['icon', className].filter(Boolean).join(' ')}
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden={isDecorative ? 'true' : undefined}
      aria-label={ariaLabel}
      role={ariaLabel ? 'img' : undefined}
      {...rest}
    >
      {iconPaths[name]}
    </svg>
  );
}

export default Icon;
